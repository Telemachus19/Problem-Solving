#include <iostream>

int main() {
  // Only one line containing the following space-separated values: int, long
  // long, char, float and double respectively.
  using namespace std;
  int x;
  long long y;
  char ch;
  float z;
  double f;
  cin >> x >> y >> ch >> z >> f;
  cout << x << endl << y << endl << ch << endl << z << endl << f << endl;
}
